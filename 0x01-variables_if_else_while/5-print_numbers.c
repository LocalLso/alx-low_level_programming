#include <stdio.h>
/**
 * main - Entry point
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
/*C program that prints all single base 10 digits*/
	/*initialize variables*/
	int singleDigNum;

	for (singleDigNum = 0 ; singleDigNum < 10 ; singleDigNum++)
	{
		printf("%d", singleDigNum"\n");
	}

	return (0);
}
